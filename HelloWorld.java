package Java;
/*
 * explicando hello world em java:
 * class -> é a classe, estrutura básica de um progrma java
 * 
 * public -> modificador de acesso, quando está "public  significa que pode ser acessado em qualquer parte
 * do código
 * 
 * static -> indica que pertence à classe, não ao objeto
 * 
 * void -> retorno da função main, siginfica que não será retornado valor algum
 * 
 * String[] args -> argumentos escritos nas linhas de comando serão passadas pra esse vetor de strings
 * 
 * System -> biblioteca padrão do Java
 * 
 * out -> rodará no console
 * 
 * println -> print é um método pra imprime algo na tela, 'ln' ele quebra linha
 */
public class HelloWorld {
    public static void main(String[] args) {
        System.out.println("Hello World!");
    }
}